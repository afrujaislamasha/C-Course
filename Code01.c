#include<stdio.h>

int main ()
{
    float a;
    double b;
    int i;

    printf("Enter float value: ");
    scanf ("%f", &a);
    printf("Float value: %f\n", a);

    printf("Enter double value: ");
    scanf ("%1f", &b);
    printf("Double value: %1f\n", b);

    printf("Enter int value: ");
    scanf ("%d", &i);
    printf("Int value: %d\n", i);

    return 0;

}
